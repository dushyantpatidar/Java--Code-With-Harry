public class tut16 {

    // do while loop 
    public static void main(String[]args){
        int a = 10 ;
        int v = 1 ;

        do{
            System.out.println(a);
            a++;
        }while(a<5);
        System.out.println("      ");
        do{
            System.out.println(v);
            v++;
        }while(v<5);
    }
}
